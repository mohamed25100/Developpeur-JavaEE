package _04_Operateurs;

public class Arithmetiques {

	public static void main(String[] args) {
		
		int var1 = 10;
		int var2;
		
		// il faut initaliser une variable avant de s'en servir
		
		var2 = 4;
		
		System.out.println("var1 + var2 = " + (var1 + var2));
		System.out.println("var1 - var2 = " + (var1 - var2));
		System.out.println("var1 * var2 = " + (var1 * var2));
		System.out.println("var1 / var2 = " + (var1 / var2));
		System.out.println("var1 % var2 = " + (var1 % var2)); // % : modulo : reste de la division ENTIERE
	}
}
